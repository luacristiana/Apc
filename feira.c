#include <stdio.h>

int main (){
    int F;
    scanf("%d", &F);

    int numero_pessoa;
    int i = 0;
    while(i < F){
        scanf("%d", numero_pessoa);

        int sorteio = 0;
        while(sorteio < numero_pessoa ){

            sorteio++;
        }

        i++;
    }

}